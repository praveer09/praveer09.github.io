// Generates public/_redirects (Cloudflare-native 301s) from the single source of
// truth: each post's `redirectFrom` frontmatter + migration/url-inventory.csv.
// Plain Node (no astro:content) so it can run as a prebuild step.
import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';
import { readInventory } from './lib/inventory.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const blogDir = join(root, 'src', 'content', 'blog');
const csvPath = join(root, 'migration', 'url-inventory.csv');
const outPath = join(root, 'public', '_redirects');

const lines = [];
const seen = new Set();
function add(from, to) {
	if (!from || !to) return;
	const key = `${from} ${to}`;
	if (seen.has(key)) return;
	seen.add(key);
	lines.push(`${from}  ${to}  301`);
}

// 1. Post redirects from each post's redirectFrom frontmatter.
const postDirs = readdirSync(blogDir, { withFileTypes: true })
	.filter((d) => d.isDirectory())
	.map((d) => d.name)
	.sort();

let postRedirectCount = 0;
for (const slug of postDirs) {
	const mdPath = ['index.md', 'index.mdx']
		.map((f) => join(blogDir, slug, f))
		.find((p) => existsSync(p));
	if (!mdPath) continue;
	const { data } = matter(readFileSync(mdPath, 'utf8'));
	for (const from of data.redirectFrom ?? []) {
		add(from, `/blog/${slug}/`);
		postRedirectCount++;
	}
}

// 2. Non-post rows from the inventory (feed, about, sitemap, archive, tagged, etc.).
const rows = readInventory(csvPath);
const parked = [];
let otherCount = 0;
for (const row of rows) {
	if (row.kind === 'post') {
		// Post paths are covered by redirectFrom above; track parked (Medium-only).
		if (!row.new_path) parked.push(row.old_path);
		continue;
	}
	if (!row.new_path || row.old_path === row.new_path) continue;
	add(row.old_path, row.new_path);
	otherCount++;
}

const header = [
	'# generated by scripts/gen-redirects.mjs - do not edit by hand',
	`# ${postRedirectCount} post redirects, ${otherCount} other redirects`,
];
if (parked.length) {
	header.push(
		`# parked (Medium-only, recreate later - no redirect emitted): ${parked.join(', ')}`,
	);
}

writeFileSync(
	outPath,
	header.join('\n') + '\n' + lines.join('\n') + '\n',
	'utf8',
);
console.log(
	`Wrote ${outPath}: ${lines.length} redirects ` +
		`(${postRedirectCount} post, ${otherCount} other, ${parked.length} parked/skipped).`,
);
